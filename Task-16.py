#Removing last element in a list
List=["Apple","Banana","Mango","Berry"]
print("Original List is:",List)
last_List=List.pop()
print("Last element in the list is:",last_List)
print("List after removing the last element is:",List)

#Removing first element from the list

List=["Apple","Banana","Mango","Berry"]
print("Original List is:",List)
first_List=List.pop(0)
print("First element in the list is:",first_List)
print("List after removing the first element is:",List)